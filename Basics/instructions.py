#in python we don't must put ; before an instruction

#if -> a conditional instruction

if 3 > 2 :
    print(" 3 > 2")
elif 3 == 3 :
    print("3 == 3")
else :
    print(" 2 < 3") 

#for -> a repetitive instruction with a known numbers of steps

for x in range(0,5):
    print(x)

#while -> a repetitive instruction with a unknown numbers of steps
a = 0
while a < 5 :
    print(a)
    a += 1

